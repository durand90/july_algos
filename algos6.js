/* A phrase is a palindrone if, after converting all uppercase letters into lowercse 
letters and removing all non-alphanumeric characters, it reads the same 
forward and backward. */

// Given a string "s", return true if it is a palindrone, or false otherwise. //

var s = "a man, a plan, a canal: Panama";


var isPalindrone = function(s) {
    s.split(" ");
}

console.log(isPalindrone(s));